var BASE_PROJECT_URL;


function getProjectPrefix (absoluteURL) {
  var firstSingleSlashIdx = absoluteURL.search(/[^\/\/]\/[^\/\/]/) + 1;
  return absoluteURL.substr(firstSingleSlashIdx).split('/')[1];
}


function getBaseProjectURL (url) {

  if (typeof BASE_PROJECT_URL === 'undefined') {
    var a = document.createElement('a');
    a.href = ''; // trigger intialization of href from host

    var absoluteURL = a.href;

    if (~a.href.indexOf('www.sipple.io')) {
      BASE_PROJECT_URL = '/' + getProjectPrefix(absoluteURL) + '/';

    } else {
      BASE_PROJECT_URL = '/';
    }
  }
  return BASE_PROJECT_URL;
}


